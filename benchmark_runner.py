def run_benchmark(
    dataset: list[BenchmarkEntry],
    config: dict,
    params: dict,
) -> list[BenchmarkResult]:
    """Run a benchmark evaluation against both SageMaker endpoints.

    Iterates over every entry in *dataset*, encodes the image, invokes
    both the quantized and full-precision endpoints, and computes
    per-entry quality metrics (exact match, BLEU, ROUGE-L) by comparing
    the generated answers to the expected answer.

    Entries that fail (missing image, endpoint error) are recorded with
    an error message and included in the returned list so that
    :func:`compute_degradation_report` can filter them appropriately.

    Args:
        dataset: The benchmark entries to evaluate.
        config: A dict containing ``quantized_endpoint``,
            ``full_precision_endpoint``, and ``aws_region``.
        params: Generation parameters (e.g. ``max_tokens``,
            ``temperature``) forwarded to both payload builders.

    Returns:
        A list of :class:`BenchmarkResult` objects, one per entry in
        *dataset*.
    """
    from comparison_utils import (
        build_full_precision_payload,
        build_quantized_payload,
        encode_image,
        invoke_endpoint,
    )

    runtime_client = boto3.client(
        "sagemaker-runtime", region_name=config["aws_region"]
    )

    results: list[BenchmarkResult] = []

    for entry in dataset:
        # --- encode image ---------------------------------------------------
        try:
            image_b64 = encode_image(entry.image_path)
        except Exception as exc:
            logger.warning(
                "Skipping entry (image_path=%s): %s", entry.image_path, exc
            )
            results.append(
                BenchmarkResult(
                    entry=entry,
                    quantized_answer="",
                    full_precision_answer="",
                    quantized_metrics={m: 0.0 for m in _METRICS},
                    full_precision_metrics={m: 0.0 for m in _METRICS},
                    error=f"Image encoding error: {exc}",
                )
            )
            continue

        # --- build payloads --------------------------------------------------
        q_payload = build_quantized_payload(entry.prompt, image_b64, params)
        fp_payload = build_full_precision_payload(entry.prompt, image_b64, params)

        # --- invoke endpoints ------------------------------------------------
        q_result = invoke_endpoint(
            config["quantized_endpoint"], q_payload, runtime_client
        )
        fp_result = invoke_endpoint(
            config["full_precision_endpoint"], fp_payload, runtime_client
        )

        # --- check for endpoint errors ---------------------------------------
        if q_result.error is not None or fp_result.error is not None:
            error_parts: list[str] = []
            if q_result.error is not None:
                error_parts.append(f"Quantized: {q_result.error}")
            if fp_result.error is not None:
                error_parts.append(f"Full-precision: {fp_result.error}")
            error_msg = "; ".join(error_parts)

            logger.warning(
                "Endpoint error for entry (image_path=%s): %s",
                entry.image_path,
                error_msg,
            )
            results.append(
                BenchmarkResult(
                    entry=entry,
                    quantized_answer=q_result.generated_text,
                    full_precision_answer=fp_result.generated_text,
                    quantized_metrics={m: 0.0 for m in _METRICS},
                    full_precision_metrics={m: 0.0 for m in _METRICS},
                    error=error_msg,
                )
            )
            continue

        # --- compute quality metrics -----------------------------------------
        q_answer = q_result.generated_text
        fp_answer = fp_result.generated_text

        quantized_metrics = {
            "exact_match": compute_exact_match(q_answer, entry.expected_answer),
            "bleu": compute_bleu_score(q_answer, entry.expected_answer),
            "rouge_l": compute_rouge_l_score(q_answer, entry.expected_answer),
        }
        full_precision_metrics = {
            "exact_match": compute_exact_match(fp_answer, entry.expected_answer),
            "bleu": compute_bleu_score(fp_answer, entry.expected_answer),
            "rouge_l": compute_rouge_l_score(fp_answer, entry.expected_answer),
        }

        results.append(
            BenchmarkResult(
                entry=entry,
                quantized_answer=q_answer,
                full_precision_answer=fp_answer,
                quantized_metrics=quantized_metrics,
                full_precision_metrics=full_precision_metrics,
                error=None,
            )
        )

    return results
