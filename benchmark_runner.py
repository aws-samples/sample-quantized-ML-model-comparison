"""Benchmark evaluation module for quantized vs. full-precision model comparison.

This module provides data structures and functions for objectively evaluating
the quality degradation introduced by model quantization. It loads benchmark
datasets (user-provided CSV/JSON or a built-in default), runs inference against
both quantized and full-precision SageMaker endpoints, computes quality metrics
(exact match, BLEU, ROUGE-L), and produces degradation reports comparing the
two models.
"""

from __future__ import annotations

import csv
import json
import logging
import os
from dataclasses import dataclass

import boto3


@dataclass
class BenchmarkEntry:
    """A single benchmark test case.

    Represents one image-prompt-expected_answer triple from a benchmark
    dataset, optionally tagged with a category for per-category breakdowns.

    Attributes:
        image_path: Local path or URL to the test image.
        prompt: The question or instruction to send to the model.
        expected_answer: The ground-truth expected answer for evaluation.
        category: Optional category for grouping results
            (e.g., "VQA", "OCR", "image_description").
    """

    image_path: str
    prompt: str
    expected_answer: str
    category: str | None


@dataclass
class BenchmarkResult:
    """Result of evaluating a single benchmark entry against both models.

    Contains the generated answers from both the quantized and full-precision
    models, along with per-entry quality metrics and any error information.

    Attributes:
        entry: The original benchmark entry that was evaluated.
        quantized_answer: The answer generated by the quantized model.
        full_precision_answer: The answer generated by the full-precision model.
        quantized_metrics: Quality metrics for the quantized model's answer,
            e.g. ``{"exact_match": 0.0, "bleu": 0.45, "rouge_l": 0.62}``.
        full_precision_metrics: Quality metrics for the full-precision model's
            answer, in the same format as ``quantized_metrics``.
        error: Error message if evaluation failed for this entry, or ``None``
            if the entry was evaluated successfully.
    """

    entry: BenchmarkEntry
    quantized_answer: str
    full_precision_answer: str
    quantized_metrics: dict[str, float]
    full_precision_metrics: dict[str, float]
    error: str | None


@dataclass
class DegradationReport:
    """Aggregate quality comparison between quantized and full-precision models.

    Summarises the overall quality metric scores for both models, the absolute
    and relative differences, optional per-category breakdowns, and the full
    list of per-entry results.

    Attributes:
        quantized_scores: Aggregate quality scores for the quantized model,
            e.g. ``{"exact_match": 0.7, "bleu": 0.65, "rouge_l": 0.72}``.
        full_precision_scores: Aggregate quality scores for the full-precision
            model, in the same format as ``quantized_scores``.
        absolute_diff: Per-metric absolute difference computed as
            ``quantized_score - full_precision_score``.
        relative_pct_change: Per-metric relative percentage change computed as
            ``(quantized - full_precision) / full_precision * 100``.
        per_category: Per-category breakdowns of scores if categories are
            present in the dataset, or ``None`` if no categories were provided.
        entry_results: All per-entry ``BenchmarkResult`` objects from the
            evaluation run.
    """

    quantized_scores: dict[str, float]
    full_precision_scores: dict[str, float]
    absolute_diff: dict[str, float]
    relative_pct_change: dict[str, float]
    per_category: dict[str, dict] | None
    entry_results: list[BenchmarkResult]


