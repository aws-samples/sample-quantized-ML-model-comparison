REQUIRED_COLUMNS = {"image_path", "prompt", "expected_answer"}


def get_default_dataset() -> list[BenchmarkEntry]:
    """Return the built-in default benchmark dataset.

    Loads the benchmark entries from ``default_benchmark.json`` located
    alongside this module.  The dataset contains 12 entries covering VQA,
    OCR, image description, scene description, and object identification
    tasks using the six test images in ``test_images/``.

    If the JSON file cannot be found (should not happen in a normal
    checkout), the function falls back to a hardcoded list of entries so
    that callers always receive usable data.

    Returns:
        A list of ``BenchmarkEntry`` objects.
    """
    module_dir = os.path.dirname(os.path.abspath(__file__))
    json_path = os.path.join(module_dir, "default_benchmark.json")

    if os.path.isfile(json_path):
        with open(json_path, encoding="utf-8") as fh:
            data = json.load(fh)
        return [
            BenchmarkEntry(
                image_path=item["image_path"],
                prompt=item["prompt"],
                expected_answer=item["expected_answer"],
                category=item.get("category") or None,
            )
            for item in data
        ]

    # Hardcoded fallback — keeps the function working even without the
    # JSON file (e.g. in minimal test environments).
    return [
        BenchmarkEntry("test_images/cat_snow.jpg", "What animal is in this image?", "cat", "VQA"),
        BenchmarkEntry("test_images/cat_snow.jpg", "Describe the setting of this image in one sentence.", "A cat is sitting in the snow outdoors.", "scene_description"),
        BenchmarkEntry("test_images/hollywood_sign.jpg", "What text is visible in this image?", "HOLLYWOOD", "OCR"),
        BenchmarkEntry("test_images/hollywood_sign.jpg", "What landmark is shown?", "The Hollywood Sign", "VQA"),
        BenchmarkEntry("test_images/market.jpg", "Describe this image in one sentence.", "An outdoor market with various stalls and goods.", "image_description"),
        BenchmarkEntry("test_images/market_singapore.jpg", "What is the main subject of this image?", "A market or street food area", "object_identification"),
        BenchmarkEntry("test_images/shibuya_crossing.jpg", "What city is this likely in?", "Tokyo", "scene_description"),
        BenchmarkEntry("test_images/shibuya_crossing.jpg", "Is this a busy or quiet scene?", "busy", "VQA"),
        BenchmarkEntry("test_images/street_seoul.jpg", "Describe the setting.", "A street scene in an urban area", "scene_description"),
        BenchmarkEntry("test_images/street_seoul.jpg", "Are there any signs with text visible?", "Yes", "OCR"),
        BenchmarkEntry("test_images/market.jpg", "List the main objects visible.", "Stalls, goods, people", "object_identification"),
        BenchmarkEntry("test_images/cat_snow.jpg", "What is happening in this image?", "A cat is in a snowy outdoor setting", "image_description"),
    ]


def _validate_file_path(file_path: str) -> str:
    """Validate that a file path is safe to read.

    Resolves symlinks and verifies the resolved path is within the
    current working directory.  This prevents path-traversal attacks
    that could read arbitrary files on the filesystem.

    Args:
        file_path: The file path to validate.

    Returns:
        The resolved absolute path if validation passes.

    Raises:
        ValueError: If the path resolves to a location outside the
            current working directory.
    """
    cwd = os.path.realpath(os.getcwd())
    resolved = os.path.realpath(os.path.join(cwd, file_path))
    if not resolved.startswith(cwd + os.sep) and resolved != cwd:
        raise ValueError(
            f"File path '{file_path}' resolves outside the working directory. "
            "Only relative paths within the project are allowed."
        )
    return resolved


def load_benchmark_dataset(file_path: str | None = None) -> list[BenchmarkEntry]:
    """Load a benchmark dataset from a CSV or JSON file.

    If *file_path* is ``None`` or the file does not exist, the function
    falls back to :func:`get_default_dataset`.  The file format is
    detected by extension (``.csv`` or ``.json``).  Required columns /
    keys are ``image_path``, ``prompt``, and ``expected_answer``.  An
    optional ``category`` column / key is used when present.

    Args:
        file_path: Path to a CSV or JSON benchmark dataset file, or
            ``None`` to use the built-in default dataset.

    Returns:
        A list of :class:`BenchmarkEntry` objects parsed from the file,
        or the default dataset when no valid file is provided.

    Raises:
        ValueError: If the file has an unsupported extension, the path
            resolves outside the working directory, or required columns
            / keys are missing from the dataset.
    """
    if file_path is None:
        return get_default_dataset()

    # Validate the path is safe before checking existence
    try:
        resolved_path = _validate_file_path(file_path)
    except ValueError:
        raise

    if not os.path.isfile(resolved_path):
        return get_default_dataset()

    _, ext = os.path.splitext(file_path)
    ext = ext.lower()

    if ext == ".csv":
        return _load_csv(resolved_path)
    elif ext == ".json":
        return _load_json(resolved_path)
    else:
        raise ValueError(
            f"Unsupported file format '{ext}'. Use .csv or .json"
        )


def _load_csv(resolved_path: str) -> list[BenchmarkEntry]:
    """Parse a CSV benchmark dataset file.

    Args:
        resolved_path: Absolute, validated path to the CSV file.

    Returns:
        A list of :class:`BenchmarkEntry` objects.

    Raises:
        ValueError: If required columns are missing from the CSV header.
    """
    with open(resolved_path, newline="", encoding="utf-8") as fh:
        reader = csv.DictReader(fh)
        fieldnames = set(reader.fieldnames or [])

        missing = REQUIRED_COLUMNS - fieldnames
        if missing:
            sorted_missing = sorted(missing)
            raise ValueError(
                f"Missing required columns: {', '.join(sorted_missing)}"
            )

        entries: list[BenchmarkEntry] = []
        for row in reader:
            category = row.get("category") or None
            entries.append(
                BenchmarkEntry(
                    image_path=row["image_path"],
                    prompt=row["prompt"],
                    expected_answer=row["expected_answer"],
                    category=category,
                )
            )
    return entries


def _load_json(resolved_path: str) -> list[BenchmarkEntry]:
    """Parse a JSON benchmark dataset file.

    Args:
        resolved_path: Absolute, validated path to the JSON file.

    Returns:
        A list of :class:`BenchmarkEntry` objects.

    Raises:
        ValueError: If the JSON is not a list of objects or required
            keys are missing.
    """
    with open(resolved_path, encoding="utf-8") as fh:
        data = json.load(fh)

    if not isinstance(data, list):
        raise ValueError("JSON benchmark dataset must be a list of objects")

    if len(data) == 0:
        return []

    # Validate required keys against the first entry
    first_keys = set(data[0].keys())
    missing = REQUIRED_COLUMNS - first_keys
    if missing:
        sorted_missing = sorted(missing)
        raise ValueError(
            f"Missing required columns: {', '.join(sorted_missing)}"
        )

    entries: list[BenchmarkEntry] = []
    for item in data:
        category = item.get("category") or None
        entries.append(
            BenchmarkEntry(
                image_path=item["image_path"],
                prompt=item["prompt"],
                expected_answer=item["expected_answer"],
                category=category,
            )
        )
    return entries

