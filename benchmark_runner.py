# Degradation Report
# ---------------------------------------------------------------------------

logger = logging.getLogger(__name__)

_METRICS = ("exact_match", "bleu", "rouge_l")


def compute_degradation_report(
    results: list[BenchmarkResult],
) -> DegradationReport:
    """Aggregate per-entry benchmark results into a degradation report.

    Filters out entries with errors, computes the mean of each quality
    metric for both the quantized and full-precision models, and derives
    absolute and relative differences.  If entries carry category labels,
    per-category breakdowns are included.

    Args:
        results: A list of ``BenchmarkResult`` objects from a benchmark
            run.  Entries whose ``error`` field is not ``None`` are
            excluded from metric aggregation but are still stored in
            ``entry_results``.

    Returns:
        A :class:`DegradationReport` summarising the quality comparison.
        If no valid (error-free) entries exist, all scores and
        differences are ``0.0``.
    """
    valid = [r for r in results if r.error is None]

    if not valid:
        zero_scores: dict[str, float] = {m: 0.0 for m in _METRICS}
        return DegradationReport(
            quantized_scores=dict(zero_scores),
            full_precision_scores=dict(zero_scores),
            absolute_diff=dict(zero_scores),
            relative_pct_change=dict(zero_scores),
            per_category=None,
            entry_results=list(results),
        )

    quantized_scores: dict[str, float] = {}
    full_precision_scores: dict[str, float] = {}
    absolute_diff: dict[str, float] = {}
    relative_pct_change: dict[str, float] = {}

    for metric in _METRICS:
        q_mean = sum(r.quantized_metrics[metric] for r in valid) / len(valid)
        fp_mean = sum(r.full_precision_metrics[metric] for r in valid) / len(valid)

        quantized_scores[metric] = q_mean
        full_precision_scores[metric] = fp_mean
        absolute_diff[metric] = q_mean - fp_mean

        if q_mean == 0.0 and fp_mean == 0.0:
            relative_pct_change[metric] = 0.0
        elif fp_mean == 0.0:
            relative_pct_change[metric] = float("inf")
        else:
            relative_pct_change[metric] = (q_mean - fp_mean) / fp_mean * 100

    # Per-category breakdowns
    per_category: dict[str, dict] | None = None
    has_categories = any(r.entry.category is not None for r in valid)

    if has_categories:
        groups: dict[str, list[BenchmarkResult]] = {}
        for r in valid:
            cat = r.entry.category or "uncategorized"
            groups.setdefault(cat, []).append(r)

        per_category = {}
        for cat, cat_results in groups.items():
            cat_q: dict[str, float] = {}
            cat_fp: dict[str, float] = {}
            cat_abs: dict[str, float] = {}
            cat_rel: dict[str, float] = {}

            for metric in _METRICS:
                cq = sum(r.quantized_metrics[metric] for r in cat_results) / len(cat_results)
                cfp = sum(r.full_precision_metrics[metric] for r in cat_results) / len(cat_results)

                cat_q[metric] = cq
                cat_fp[metric] = cfp
                cat_abs[metric] = cq - cfp

                if cq == 0.0 and cfp == 0.0:
                    cat_rel[metric] = 0.0
                elif cfp == 0.0:
                    cat_rel[metric] = float("inf")
                else:
                    cat_rel[metric] = (cq - cfp) / cfp * 100

            per_category[cat] = {
                "quantized_scores": cat_q,
                "full_precision_scores": cat_fp,
                "absolute_diff": cat_abs,
                "relative_pct_change": cat_rel,
            }

    return DegradationReport(
        quantized_scores=quantized_scores,
        full_precision_scores=full_precision_scores,
        absolute_diff=absolute_diff,
        relative_pct_change=relative_pct_change,
        per_category=per_category,
        entry_results=list(results),
    )


