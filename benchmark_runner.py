# Quality Metrics
# ---------------------------------------------------------------------------


def compute_exact_match(generated: str, expected: str) -> float:
    """Compute exact match between generated and expected answers.

    Performs a case-insensitive comparison after stripping leading and
    trailing whitespace from both strings.

    Args:
        generated: The model-generated answer.
        expected: The ground-truth expected answer.

    Returns:
        ``1.0`` if the normalised strings are equal, ``0.0`` otherwise.
    """
    return 1.0 if generated.strip().lower() == expected.strip().lower() else 0.0


def compute_bleu_score(generated: str, expected: str) -> float:
    """Compute BLEU score between generated and expected answers.

    Uses ``nltk.translate.bleu_score.sentence_bleu`` with uniform
    1-4 gram weights and Method 1 smoothing to handle short sentences.
    Tokenisation is performed by whitespace splitting.

    Args:
        generated: The model-generated answer.
        expected: The ground-truth expected answer.

    Returns:
        A float in ``[0.0, 1.0]``.  Returns ``0.0`` if either string
        is empty after stripping whitespace.
    """
    from nltk.translate.bleu_score import SmoothingFunction, sentence_bleu

    gen_tokens = generated.split()
    ref_tokens = expected.split()

    if not gen_tokens or not ref_tokens:
        return 0.0

    smoothing = SmoothingFunction().method1
    return float(
        sentence_bleu([ref_tokens], gen_tokens, smoothing_function=smoothing)
    )


def compute_rouge_l_score(generated: str, expected: str) -> float:
    """Compute ROUGE-L F-measure between generated and expected answers.

    Uses ``rouge_score.rouge_scorer.RougeScorer`` with the ``rougeL``
    metric.

    Args:
        generated: The model-generated answer.
        expected: The ground-truth expected answer.

    Returns:
        A float in ``[0.0, 1.0]``.  Returns ``0.0`` if either string
        is empty after stripping whitespace.
    """
    from rouge_score import rouge_scorer

    if not generated.strip() or not expected.strip():
        return 0.0

    scorer = rouge_scorer.RougeScorer(["rougeL"], use_stemmer=False)
    scores = scorer.score(expected, generated)
    return float(scores["rougeL"].fmeasure)

